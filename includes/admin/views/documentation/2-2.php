			Columns can be used to position fields next to eachother, or conditionally hide a set of fields.
			They also have the option to be invisible, and they have the ability to be duplicated dynamically by the user.
			This comes in handy when you want to allow a visitor to enter multiple user data.<br /><br />
			<strong>Multi-parts:</strong><br />
			A multi-part can be used to convert your form into multiple sections/steps (also know as multi-step).
			It is an easy way to break down the form in multiple sections that makes it in some cases more userfriendly to be filled out by the visitor.
		<td>
